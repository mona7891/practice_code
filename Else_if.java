import java.util.*;
import java.io.*;
class Else_if
{
	public static void main(String args[])
	{
		Scanner sc = new Scanner(System.in);

		int a = sc.nextInt();
		int b = sc.nextInt();

		if(a==b)
		{
			System.out.println("A AND B ARE EQUAL !!");
		}
		else if(a>b)
		{
			System.out.println("A IS  GHETER THAN B  !!");
		}
		else
		{
			 System.out.println("A IS LESSER THAN B  !!");

		}
		
	}
}
